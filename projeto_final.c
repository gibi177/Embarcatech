//função para printar texto do menu inicial no display
void print_text(uint8_t *ssd, struct render_area *frame_area) {
    char *text[] = {
        "Aperte o botao",
        "A para abelha",
        "B para gato",
        "2xA para flor",
        "2xB para balao"
    };

    int y = 0;
    for (uint i = 0; i < count_of(text); i++) {
        ssd1306_draw_string(ssd, 5, y, text[i]);
        y += 8;
    }
    render_on_display(ssd, frame_area);
}

//função para checar quantidade de pressionamentos do botão
int check_button_press(uint button_pin) {
    uint press_count = 0;
    bool is_pressed = false;
    uint64_t press_time = time_us_64();  // Marca o tempo de início

    // Espera 500 ms (tempo para detectar um ou dois cliques)
    while (time_us_64() - press_time < 500000) {
        if (gpio_get(button_pin) == 0 && !is_pressed) { // Verifica se o botão foi pressionado
            is_pressed = true;
            press_count++;
            sleep_us(50);  // Pequeno delay para evitar leitura múltipla
        }
        if (gpio_get(button_pin) == 1 && is_pressed) {  // Detecta borda de subida (soltou)
            is_pressed = false;
        }
    }

    return press_count;
}

int main()
{
    stdio_init_all();

    // Configura os pinos dos botões como entradas com resistores de pull-up
    gpio_init(BUTTON_A);
    gpio_set_dir(BUTTON_A, GPIO_IN);
    gpio_pull_up(BUTTON_A);

    gpio_init(BUTTON_B);
    gpio_set_dir(BUTTON_B, GPIO_IN);
    gpio_pull_up(BUTTON_B);

    // Inicialização do i2c
    i2c_init(i2c1, ssd1306_i2c_clock * 5000);
    gpio_set_function(I2C_SDA, GPIO_FUNC_I2C);
    gpio_set_function(I2C_SCL, GPIO_FUNC_I2C);
    gpio_pull_up(I2C_SDA);
    gpio_pull_up(I2C_SCL);

    
    // Preparar área de renderização para o display
    struct render_area frame_area = {
        start_column : 0,
        end_column : ssd1306_width - 1,
        start_page : 0,
        end_page : ssd1306_n_pages - 1
    };
    
    calculate_render_area_buffer_length(&frame_area);
    
    //buffer principal do display
    uint8_t ssd[ssd1306_buffer_length];
    
    while (true) {

        // Processo de inicialização do OLED
        ssd1306_init();
        
        //zera o display
        memset(ssd, 0, ssd1306_buffer_length);
        render_on_display(ssd, &frame_area);

        //Printa texto do menu inicial
        print_text(ssd, &frame_area);

        const uint8_t* final_bitmap = NULL;  
        while (true) {
            // Verifica o Botão A
            if (gpio_get(BUTTON_A) == 0) {  // Se o botão A for pressionado (ativo baixo)
                int bt_a_count = check_button_press(BUTTON_A);  // Chama a função para contar as pressões

                if (bt_a_count == 1) {
                    final_bitmap = bitmap_bee_128x64;
                } else if (bt_a_count == 2) {
                    final_bitmap = bitmap_flower_128x64;
                }
                break;
            } 

            // Verifica o Botão B
            else if (gpio_get(BUTTON_B) == 0) {  // Se o botão B for pressionado (ativo baixo)
                int bt_b_count = check_button_press(BUTTON_B);  // Chama a função para contar as pressões

                if (bt_b_count == 1) {
                    final_bitmap = bitmap_cat_128x64;
                } else if (bt_b_count == 2) {
                    final_bitmap = bitmap_balloon_128x64;
                }
                break;
            }

            sleep_ms(100);  // Delay para evitar leituras excessivas
        }

        //Declaração do buffer para o desenho
        ssd1306_t ssd_bm;
        ssd1306_init_bm(&ssd_bm, 128, 64, false, 0x3C, i2c1);
        ssd1306_config(&ssd_bm);

        // zera o display inteiro
        memset(ssd, 0, ssd1306_buffer_length);
        render_on_display(ssd, &frame_area);

        //Desenha o bitmap escolhido pelo usuário
        ssd1306_draw_bitmap(&ssd_bm, final_bitmap);

        sleep_ms(3000); //Aguarda 3s antes de reiniciar o loop, voltando ao menu principal
    }

    return 0;
}
